<div class="text-block introduction">
	<p>Er sei zufällig in der Nähe dreier Morde gewesen, schreibt er: Beängstigend dreht sich alles mäanderhaft in seinem Kopf, die Mörder, die Täter, die Opfer, die Polizisten, die Medien, und wie in einer Waschmaschine werden die Kontrahenten, die Einzelteile, die Details, die Waffen, die Schatten, die Bars durcheinander gewirbelt. Und er? Der Geiser? Kann er das in seinem Gehirn zusammen- oder auseinanderhalten?</p>
	<p>Man zweifelt, als Zeuge für eine einzige Wahrheit ist er sehr suspekt. Irrlichternd schreibt und schreitet er durch die Versatzstücke hilarisch, wunderbar schräg, drei wahre Morde, leider ein dreimaliges Vergnügen.</p>
	<p class="author-signature">Clemens Klopfenstein</p>
