<div class="text-block author-bio">
	<p>1949 in Basel geboren, lebt als freier Schriftsteller in Bern und Berlin.</p>
	
	<p>Seit 1968 veröffentlicht er Gedichte, Erzählungen und Romane, unter anderem die grossbürgerlichen Familienromane “<i>Grünsee</i>” und “<i>Brachland<i>”, die Künstlerromane “<i>Das geheime Fieber</i>” über den Barockmaler Caravaggio und “<i>Das Gefängnis der Wünsche</i>” über den Marquis de Sade und Goethe.</p>
	
	<p>Zuletzt erschien 2013 im Offizin Verlag Zürich “<i>Schöne Bescherung. Kein Familienroman</i>”.</p>

	<p class="author-signature">C. G.</p>
</div>
