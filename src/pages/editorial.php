	<p>Liebe Freunde des guten Buches,</p>

	<p>neben unseren Reisebüchern, die in der Reihe &ldquo;kommedia.edizioni&rdquo; erscheinen, werden wir uns ab Herbst 2011 auch auf das Gebiet der Belletristik wagen. In der Reihe &ldquo;Die Lunte&rdquo; werden wir in loser Folge schräge &ndash; schlimmer noch! &ldquo;üble&rdquo; Literatur auf den Markt bringen.</p>

	<p>Die beiden ersten &ldquo;üblen Bücher&rdquo; stammen von zwei Autoren aus der Schweiz: Clemens Klopfenstein und Marcus P. Nester &mdash; ein Autorenduo, das sich schon bei anderen Buch-und Filmprojekten bewährt hat und mit dem gemeinsam verfassten Buch &ldquo;Die Migros-Erpressung&rdquo; (zytglogge/rororo-thriller) grosses Aufsehen erregte. Walter Vogt schrieb damals in der NZZ: &ldquo;Die Schweiz hat einen neuen Kriminalroman!&rdquo; Nach einer Auflage von 50 000 Exemplaren wurde das Buch jedoch plötzlich vom Markt genommen, und die im Titel genannte Supermarktkette verbot jegliche weitere Auslieferung. Später arbeiteten die beiden Autoren zusammen am Schweizer-Tatort &ldquo;Alp-Traum&rdquo;, an dem unter anderen Stefan Kurt, Delia Meyer und Caroline Redl mitwirkten. „Alp-Traum“ erzielte eine der höchsten Einschaltquoten in der Schweiz.</p>

	<p>Diesmal kommen die beiden Autoren einzeln daher, doch man spürt ihren Zusammenhang, so dass wir die beiden Romane gleichzeitig zu veröffentlichen.</p>

	<p>&ldquo;<a href="?p=vergiss-venedig-nester">Vergiss Venedig</a>&rdquo; von Marcus P. Nester und &ldquo;<a href="?p=schwein-gehabt-klopfenstein">Schwein gehabt</a>&rdquo; von Clemens Klopfenstein scheinen auf den ersten Blick wieder Kriminalromane zu sein: der eine bewegt sich vom Zürcher Fernsehen ans Festival von Venedig und endet unglücklich (glücklich?) tödlich. Der andere spielt im dunklen Umbrien und endet in der Ekstase.</p>

	<p>Doch immer wieder schweift die Handlung der beiden Romane vom reinen Krimiplot ab, der Text entgleitet, rutscht richtiggehend aus, wird schräg und kann manchmal sogar übel abstürzen. Üble Romane halt – doch urteilen Sie selbst!</p>

	<p>Wir freuen uns, wenn Sie auf die beiden Bücher neugierig geworden sind. Bitte kaufen Sie gleich beide im Doppelpack, denn sonst gibt’s bei uns im Hause nur Neid und Missgunst.</p>

