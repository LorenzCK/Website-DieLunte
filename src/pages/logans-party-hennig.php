<div class="text-block introduction">
	<p>Daniel wird von seiner Geliebten Anna verlassen, und sein Freund Tony verschwindet spurlos. Er versucht Anna zurückzugewinnen, aber sie hat bereits einen anderen. Auf der Suche nach Tony reist Daniel nach England. Tonys Zwillingsschwester Celia erzählt ihm, Tony habe sich von einer Klippe ins Meer gestürzt&hellip;</p>

	<p>Eine Zeitreise in die frühen 1990er-Jahre und in die Glücksillusion der Droge. In jene Sehnsucht nach ewiger Jugend und niemals zu Ende gehenden weissen Nächten mit ihrer Verheissung von Glanz und Sinngebung.</p>

	<p>Eine Welt als Angebot, in der Koks teurer, aber auch besser ist als heute, und zwanghaft Spontansex im Plauderton vollzogen wird.</p>
</div>

