<div class="text-block introduction">
	<p>Mathieu verliert sein Herz an Sarah, die Moderatorin der Wettersendung &ldquo;Räge, Blitz und Sunneschtrahl&rdquo;. Die Aussichten für die junge Liebe scheinen, trotz einiger Nebelschwaden, sehr gut zu sein. Doch dann verliert Mathieu zuerst seinen Job und dann die Kontrolle über sein Auto.</p>
	<p>Tödlich verunfallt, landet er in einer Welt, die ihren ganz eigenen Gesetzen folgt. Auch Sarahs Welt verändert sich, als sie sich auf die Suche nach Mathieu begibt. Es scheint, der Teufel habe seine Hand im Spiel. Aber gibt es den Teufel überhaupt?</p>
	<p>Eine Erzählung voller überraschender Wendungen und ironischer Pointen, mit blitzenden Dialogen und einem sunneschtrahlenden Surrealismus.</p>
</div>

