<div class="text-block author-bio">
	<p>Martin Hennig, geboren 1951 in Basel.</p>

	<p>Veröffentlichte Romane, darunter &ldquo;<i>Das geübte Lächeln</i>&rdquo; (1981) und Filme wie &ldquo;<i>Erinnerung an die Leidenschaft</i>&rdquo; (1976). 1984&nbsp;&mdash;&nbsp;1994 Dramaturg beim Schweizer Fernsehen,
	Zürich.</p>

	<p>Seit 1994 Autor und Dramaturg in Zürich und Köln. Arbeitet an einem Roman über die Filmbranche.</p>

	<p class="author-signature">M. H.</p>
