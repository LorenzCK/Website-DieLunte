<div class="press">
	<div class="author">Rosmarie Zeller</div>
	<div class="media">ProgrammZeitung, Basel</div>
	<div class="date">November 2016</div>

	<p>Christoph Geiser meldet sich in seinem jüngsten Buch mit “Mordsachen” zu Wort. Der Band enthält drei Geschichten, die erste (“Und immer wär’ Februar”) spielt in Leipzig und behandelt einen Mord an einem Kind, die zweite (“Das Lächeln der Uhren”) in Berlin und hat einen Raubmord zum Gegenstand, der als Sexualmord getarnt wird, und die dritte schliesslich, die am weitesten zurückliegt, berichtet über einen Sexualmord an einem jungen Mann in Australien (“Down under”).</p>
    <p>Der Schriftsteller ist mit seinem eigenen Begehren und seiner Fantasie in die Geschichten verwickelt, die er mittels Internet und Quellenstudium zu rekonstruieren versucht. Dabei verheddert er sich, wie er einmal sagt, im “Gestrüpp” der Geschichten, weil jedes Wort, jeder Ausdruck Anlass sein können für Assoziationen, für Wortspiele und Kalauer, die plötzlich die scheinbar harmlose Berichterstattung über die Morde hintergründig werden lässt.</p>
