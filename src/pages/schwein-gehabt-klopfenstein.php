<div class="text-block center-quote introduction">
	<p>Eine unglaubliche Verkettung von schaurigen und blamablen Begegnungen, die einem abwechselnd zum Kichern zum Lautlachen oder zum Schaudern bringen. Sehr schön auch: Klopfenstein nimmt es mit der Erzählperspektive nicht so streng. Er löst sich immer vom ICH-Erzhähler und probiert ganz unbekümmert die unterschiedlichsten Anverwandlungen aus, tierische eingeschlossen. Lesen sie es in einem durch, so wie es auf dem Klappentext steht: &ldquo;wie ein Schinkenteller nach langer Wanderung&rdquo;.</p>

	<p class="ending-note"><a href="http://www.sigigoetz-entertainment.de/" target="_blank">SigiGoetz Entertainment</a>, München.</p>
</div>

