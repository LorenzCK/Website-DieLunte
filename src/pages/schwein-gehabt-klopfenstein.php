<div class="text-block author-bio">
	<p>Der Autor ist Zeichner, Maler, Regisseur und Filmproduzent. Er lebt seit 35 Jahren in Umbrien.</p>

	<p>Ein erstes Buch, zusammen mit Marcus P. Nester geschrieben, ist heute wegen Nachahmungsgefahr verboten ("Die Migros-Erpressung" Zytglogge/rororo-thriller) ein gemeinsamer Tatort hatte die höchste Einschaltquote in der Schweiz ("Alp-Traum" SRG-ARD). Er hat mit dem Film "E nachtlang Füürland" den ersten Max-Ophüls-Preis bester deutschsprachiger Nachwuchspreis für die Schweiz geholt, später in Monte-Carlo den Preis für den besten TV-Spielfilm ("Das vergessene Tal" SF/NRD/ORF/NOS).</p>

	<p>Darauf hat er mit "Das Schweigen der Männer" den neu erfundenen Schweizer Filmpreis, bester Spielfilm, erhalten, sein bislang letzter Langspielfilm "Die Vogelpredigt" erhielt zwei Nominationen. Sein neuster Film "The it.Aliens" lief am Festival in Venedig, in Arbeit ist "Figures in a nightscape". Spielfilm. Eine Ausstellung seiner Bilder ist im Januar 2012 in Solothurn geplant. Drei seiner Filme spielen in Umbrien ("Der Ruf der Sybilla", "WerAngstWolf" und "Die Vogelpredigt") wie auch das neue Buch "Schwein gehabt".</p>

