<div class="text-block introduction">
	<p>Diese drei Krimi-Erzählungen handeln von Menschen, die sich in Gefahr begeben und darin umkommen&nbsp;—&nbsp;oder fast. Die Grenzen zwischen Opfer und Täter sind fliessend: Bei einem übereifrigen Steuerbeamten und einem fast unbescholtenen Steuerzahler, bei einem allzu gerissenen Zürcher Politiker und seiner unehelichen Tochter und bei einem Ehepaar, das von gegenseitigem Misstrauen so zerfressen ist, dass es den äußeren Horror erst bemerkt, als es zu spät ist.</p>
	
	<p>Der Autor hat mit konventionellen Krimis wenig am Hut. So wird zwar auch in diesen drei Kurzgeschichten erpresst, gemordet und gestorben&nbsp;—&nbsp;doch die Polizei bleibt aussen vor. Täter und Opfer, alle haben hier ihre dunkeln Seiten, und die deckt Nester mit bissigem Humor auf.
</div>

<div class="book-title">Vergiss Venedig</div>

<div class="book-cover">
	<img src="img/erste-serie-covers/nester-vergiss-venedig.jpg" width="200" height="295" alt="Marcus P. Nester, Vergiss Venedig" srcset="img/erste-serie-covers/nester-vergiss-venedig-2x.jpg 2x, img/erste-serie-covers/nester-vergiss-venedig-3x.jpg 3x" />
</div>

<div class="text-block introduction">
	<p>&ldquo;Vergiss Venedig&rdquo; ist primär&nbsp;—&nbsp;oder nur prima vista?&nbsp;—&nbsp;kein Krimi, sondern eine Liebesgeschichte. Ein Kinobesuch liegt ihr zugrunde: &ldquo;The Bridges of Madison County&rdquo;, die traurigschöne Geschichte der Familienmutter Meryl Streep, die sich in den vorbeiziehenden Fotografen Clint Eastwood verliebt, doch letztlich entsagt und sich wieder ihrer Familie widmen wird. Eine zutiefst unglückliche Liebe.</p>

	<p>&ldquo;Vergiss Venedig&rdquo; variiert diese Situation aus der Sicht des Mannes&nbsp;—&nbsp;hier verzichtet <i>er</i>, opfert er die Liebe seines Lebens. Aber ist das Opfer nicht ein Versagen? Denn der &ldquo;Held&rdquo; ist ein Zauderer. Er zählt sich zu den zu kurz Gekommenen&nbsp;—&nbsp;und zwar dort, wo es für Männer zählt: zwischen den Beinen. André Kiefer hadert mit diesem Schicksal, und als ihm das Glück seines Lebens begegnet, traut er sich nicht, es festzuhalten. Die Amour fou eines Schweizers&nbsp;—&nbsp;kann es die geben? Mit dem Filmfestival von Venedig als Hintergrund ist &ldquo;Vergiss Venedig&rdquo; gleichzeitig ein atmosphärisch dichter, ironisch-gesellschaftskritischer Spiegel des Medienrummels und des Filmgeschäfts.</p>
</div>

<div class="section-title">Der Autor</div>

<div class="author-pic">
	<img src="img/nester_3.jpg" width="256" height="350" alt="Marcus P. Nester" />
</div>

<div class="text-block author-bio">
	<p>1947 in Basel geboren. Phil-1-Studium und Lehrer-Ausbildung, dazwischen Besuch der Filmkurse an der Kunstgewerbeschule Zürich in der Regie-Klasse unter Kurt Früh. Seither zeitweise Zusammenarbeit mit Clemens G. Klopfenstein an gemeinsamen Film- und Literaturprojekten, vom Kriminalroman &ldquo;Die Migros-Erpressung&rdquo; (Zytglogge 1978) bis zum Drehbuch &ldquo;Tatort - Alptraum&rdquo; (1998). 1971 Realisation des mittellangen Spielfilms &ldquo;Ballonbremser&rdquo;, einem der ersten drei vom Schweizer Fernsehen mitproduzierten Werke von &ldquo;Jungfilmern&rdquo;.</p>

	<p>Nach fünf Jahren als Redaktor für audiovisuelle Unterrichtsmittel in einem Basler Chemiekonzern 1979 Wechsel zum Fernsehen DRS, dort bis 2006 Redaktor für Spielfilmeinkauf und –programmierung. Daneben Veröffentlichung von Kurzgeschichten und Romanen im Krimi- und Thrillergenre wie &ldquo;Das leise Gift&rdquo; (1982, Benziger und rororo Thriller) etc. und Drehbuch-Arbeiten für die Krimiserien &ldquo;Eurocops&rdquo; und &ldquo;Tatort&rdquo;, sowie für Fernsehfilme.</p>

	<p class="author-signature">M. P. N.</p>
</div>

