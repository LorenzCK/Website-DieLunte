<div class="text-block center-quote">
	<p><b>Die Lunte</b> wurde in der windigen Nacht vom 20. zum 21. Februar 2011 von Margarete Berg, Giorgio Wolfensberger und Clemens Klopfenstein als Nebenreihe “Üble Bücher” des Verlag Kommedia (Wolfensberger-Berg), Città della Pieve, erfunden.</p>
	<p>Die erste Staffel (Marcus P. Nester “Vergiss Venedig”, Martin Hennig “Logans Party”, und Clemens Klopfenstein “Schwein gehabt”) wurde im November an der Basler Buchmesse vorgestellt und 2012 an die Solothurner Literaturtage eingeladen.</p>
	<p>Später wurde Die Lunte auf die Plattform des Spiegelberg-Verlags umgesiedelt.</p>
</div>

